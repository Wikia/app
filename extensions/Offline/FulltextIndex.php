class FulltextIndex
{
	function __construct__($path)
	{
		$this->db = new XapianDatabase($path);
	}

	function search($term)
	{
		#$qp = new XapianQueryParser();
		#$qp->set_database($db);
		#$stemmer = new XapianStem("english");
		#$qp->set_stemmer($stemmer);
		#$query = $qp->parse_query($term);

		$query = new XapianQuery($term);
		$enquire = new XapianEnquire($this->db);
		$enquire->set_query($query);
		$matches = $enquire->get_mset(0, 25);

		if (0 /*SCORING*/) {
			$scores = array();
			for ($i = $matches->begin(); !$i->equals($matches->end()); $i->next())
			{
				$row = $i->get_document();
				$str = $i->get_percent()."% [".$row->get_data()."]";
				$scores[] = $str;
				if (0/*DEBUG*/) wfDebug("$str\n");
			}
		}

		$result = array();
		for ($i = $matches->begin(); !$i->equals($matches->end()); $i->next())
		{
			$entry = $i->get_document()->get_data();
			$result[] = explode(':', $entry, 3);
		}
		# not available in Xapian 1.0.X:
		#$db->close();
		return $result;
	}
}

